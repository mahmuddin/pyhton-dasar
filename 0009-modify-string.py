print("Uppercase:")
a = "Hello, World!"
print(a.upper())
print("\n")


print("Lowercase:")
a = "Hello, World!"
print(a.lower())
print("\n")

print("Remove whitespace:")
a = "   Hello, World!   "
print(a.strip())  # returns "Hello, World!"
print("\n")


print("Replace string:")
a = "Hello, World!"
print(a.replace("H", "J"))  # returns "Jello, World!"
print("\n")


print("Split string becomes the list items:")
a = "Hello, World!"
print(a.split(","))  # returns ['Hello', ' World!']
print("\n")
